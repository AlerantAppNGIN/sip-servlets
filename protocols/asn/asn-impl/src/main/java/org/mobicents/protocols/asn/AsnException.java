package org.mobicents.protocols.asn;

/**
 * 
 * @author amit bhayani
 *
 */
public class AsnException extends Exception {

	public AsnException() {
	}

	public AsnException(String message) {
		super(message);
	}

	public AsnException(Throwable cause) {
		super(cause);
	}

	public AsnException(String message, Throwable cause) {
		super(message, cause);
	}

}
